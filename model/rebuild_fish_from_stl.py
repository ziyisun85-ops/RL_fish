def material_asset_line(name: str, rgba: str, **attrs: float | str) -> str:
    attr_text = " ".join(f'{key}="{value}"' for key, value in attrs.items())
    suffix = f" {attr_text}" if attr_text else ""
    return f'<material name="{name}" rgba="{rgba}"{suffix}/>'


