def f(value: float) -> str:
    return f"{value:.6f}".rstrip("0").rstrip(".")


def vec3(values: Iterable[float]) -> str:
    x, y, z = values
    return f"{f(x)} {f(y)} {f(z)}"


def indent(lines: list[str], level: int) -> list[str]:
    prefix = "  " * level
    return [prefix + line if line else "" for line in lines]


@dataclass
class MeshInfo:
    name: str
    file_name: str
    triangles: np.ndarray
    bbox_min: np.ndarray
    bbox_max: np.ndarray
    center: np.ndarray
    volume: float

    @property
    def size(self) -> np.ndarray:
        return self.bbox_max - self.bbox_min

    @property
    def half_size(self) -> np.ndarray:
        return self.size / 2.0


def read_binary_stl(path: Path) -> np.ndarray:
    data = path.read_bytes()
    if len(data) < 84:
        raise ValueError(f"STL too small: {path}")

    tri_count = struct.unpack("<I", data[80:84])[0]
    expected = 84 + tri_count * 50
    if expected != len(data):
        raise ValueError(f"Only binary STL is supported: {path}")

    tris = np.empty((tri_count, 3, 3), dtype=np.float64)
    offset = 84
    for idx in range(tri_count):
        values = struct.unpack("<12fH", data[offset : offset + 50])
        tris[idx, 0, :] = values[3:6]
        tris[idx, 1, :] = values[6:9]
        tris[idx, 2, :] = values[9:12]
        offset += 50
    return tris


def write_binary_stl(path: Path, triangles: np.ndarray) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    header = b"Generated by rebuild_fish_from_stl.py".ljust(80, b" ")
    payload = bytearray(header)
    payload.extend(struct.pack("<I", int(len(triangles))))

    for tri in triangles:
        v1 = tri[1] - tri[0]
        v2 = tri[2] - tri[0]
        normal = np.cross(v1, v2)
        norm = np.linalg.norm(normal)
        if norm > 0:
            normal = normal / norm
        else:
            normal = np.zeros(3, dtype=np.float64)
        record = (
            float(normal[0]),
            float(normal[1]),
            float(normal[2]),
            float(tri[0, 0]),
            float(tri[0, 1]),
            float(tri[0, 2]),
            float(tri[1, 0]),
            float(tri[1, 1]),
            float(tri[1, 2]),
            float(tri[2, 0]),
            float(tri[2, 1]),
            float(tri[2, 2]),
            0,
        )
        payload.extend(struct.pack("<12fH", *record))

    path.write_bytes(payload)


def transform_to_model(points: np.ndarray, origin_mm: np.ndarray) -> np.ndarray:
    shifted = points - origin_mm
    # Original assembly axis is +Y from head to tail. Rotate +90 deg around Z so:
    # head direction (-Y) becomes +X and the model matches the prompt convention.
    x = -shifted[..., 1]
    y = shifted[..., 0]
    z = shifted[..., 2]
    return np.stack((x, y, z), axis=-1) * 0.001


def mesh_info(name: str, file_name: str, triangles: np.ndarray) -> MeshInfo:
    pts = triangles.reshape(-1, 3)
    bbox_min = pts.min(axis=0)
    bbox_max = pts.max(axis=0)
    center = (bbox_min + bbox_max) / 2.0
    return MeshInfo(
        name=name,
        file_name=file_name,
        triangles=triangles,
        bbox_min=bbox_min,
        bbox_max=bbox_max,
        center=center,
        volume=mesh_volume(triangles),
    )


def mesh_volume(triangles: np.ndarray) -> float:
    # STL winding can be inconsistent, so accumulate signed tetra volumes
    # and take the absolute value at the end.
    v0 = triangles[:, 0, :]
    v1 = triangles[:, 1, :]
    v2 = triangles[:, 2, :]
    signed_volume = np.einsum("ij,ij->i", v0, np.cross(v1, v2)).sum() / 6.0
    return abs(float(signed_volume))


def make_local_mesh(source_path: Path, mesh_name: str) -> MeshInfo:
    source_tris = read_binary_stl(source_path)
    pts = source_tris.reshape(-1, 3)
    bbox_min = pts.min(axis=0)
    bbox_max = pts.max(axis=0)
    center = (bbox_min + bbox_max) / 2.0
    local_tris = transform_to_model(source_tris, center)
    file_name = f"{mesh_name}.stl"
    write_binary_stl(OUT_MESH_DIR / file_name, local_tris)
    return mesh_info(mesh_name, file_name, local_tris)


def split_tail_mesh(source_path: Path, centre_origin_mm: np.ndarray) -> tuple[list[MeshInfo], list[float]]:
    source_tris = read_binary_stl(source_path)
    pts = source_tris.reshape(-1, 3)
    bbox_min = pts.min(axis=0)
    bbox_max = pts.max(axis=0)
    tail_center_mm = (bbox_min + bbox_max) / 2.0

    local_tail = transform_to_model(source_tris, tail_center_mm)
    tri_centers = local_tail.mean(axis=1)
    x_min = tri_centers[:, 0].min()
    x_max = tri_centers[:, 0].max()
    edges = np.linspace(x_max, x_min, TAIL_SEG_COUNT + 1)

    global_tail_center = transform_to_model(tail_center_mm.reshape(1, 3), centre_origin_mm).reshape(3)

    meshes: list[MeshInfo] = []
    global_centers: list[float] = []
    for idx in range(TAIL_SEG_COUNT):
        upper = edges[idx]
        lower = edges[idx + 1]
        if idx < TAIL_SEG_COUNT - 1:
            mask = (tri_centers[:, 0] <= upper) & (tri_centers[:, 0] > lower)
        else:
            mask = (tri_centers[:, 0] <= upper) & (tri_centers[:, 0] >= lower)
        seg_tris = local_tail[mask]
        if len(seg_tris) == 0:
            raise ValueError(f"tail segment {idx + 1} has no triangles")

        seg_pts = seg_tris.reshape(-1, 3)
        seg_bbox_min = seg_pts.min(axis=0)
        seg_bbox_max = seg_pts.max(axis=0)
        seg_center = (seg_bbox_min + seg_bbox_max) / 2.0
        seg_local = seg_tris - seg_center

        mesh_name = f"tail_seg{idx + 1}_mesh"
        file_name = f"{mesh_name}.stl"
        write_binary_stl(OUT_MESH_DIR / file_name, seg_local)
        info = mesh_info(mesh_name, file_name, seg_local)
        meshes.append(info)
        global_centers.append(float(global_tail_center[0] + seg_center[0]))

    return meshes, global_centers


def dynamic_box_geom(name: str, half_size: np.ndarray, mass: float) -> str:
    size = np.maximum(half_size, np.array([0.002, 0.002, 0.002], dtype=np.float64))
    return (
        f'<geom name="{name}_dyn" type="box" size="{vec3(size)}" '
        f'mass="{f(mass)}" rgba="0 0 0 0" contype="1" conaffinity="2" group="4"/>'
    )


