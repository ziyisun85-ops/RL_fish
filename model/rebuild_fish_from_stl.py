def classify_vertebrae(stl_paths: list[Path]) -> tuple[list[Path], list[float], list[Path], list[float], np.ndarray]:
    rows = []
    for path in stl_paths:
        tris = read_binary_stl(path)
        pts = tris.reshape(-1, 3)
        bbox_min = pts.min(axis=0)
        bbox_max = pts.max(axis=0)
        center = (bbox_min + bbox_max) / 2.0
        rows.append((path, center))

    # All rigid meshes share the same global X/Z centre. Use the compartment as the chain origin.
    centre_path = next(path for path, _ in rows if "centre_compartment" in path.name)
    centre_tris = read_binary_stl(centre_path)
    centre_pts = centre_tris.reshape(-1, 3)
    centre_bbox_min = centre_pts.min(axis=0)
    centre_bbox_max = centre_pts.max(axis=0)
    centre_origin = (centre_bbox_min + centre_bbox_max) / 2.0

    vertebra_rows = [(path, center) for path, center in rows if "back_v1-" in path.name]
    front = sorted(
        ((path, float(-(center[1] - centre_origin[1]) * 0.001)) for path, center in vertebra_rows if center[1] < centre_origin[1]),
        key=lambda item: item[1],
    )
    back = sorted(
        ((path, float(-(center[1] - centre_origin[1]) * 0.001)) for path, center in vertebra_rows if center[1] > centre_origin[1]),
        key=lambda item: abs(item[1]),
    )

    if len(front) != 9 or len(back) != 9:
        raise ValueError(f"expected 9 front and 9 back vertebrae, got {len(front)} front and {len(back)} back")

    front_paths, front_pos = zip(*front)
    back_paths, back_pos = zip(*back)
    return list(front_paths), list(front_pos), list(back_paths), list(back_pos), centre_origin


