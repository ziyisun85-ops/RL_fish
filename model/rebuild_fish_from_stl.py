def midpoint_joint_pos(body_pos_x: float) -> str:
    # Put the hinge at the midpoint between parent and child body centers.
    return vec3((-0.5 * body_pos_x, 0.0, 0.0))


def hinge_line(name: str, joint_class: str, body_pos_x: float, enabled: bool) -> list[str]:
    if not enabled:
        return []
    return [f'<joint name="{name}" class="{joint_class}" pos="{midpoint_joint_pos(body_pos_x)}"/>']


